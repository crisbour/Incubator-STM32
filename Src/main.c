char int_to_hexString(uint8_t val){
	if(val<10) return '0'+val;

	return 'A'+val-10;
}
void rom_to_hex(char* hexStr, uint8_t rom[]){
	for(uint8_t i=0;i<8;i++){
		hexStr[15-2*i]=int_to_hexString(rom[i]&0xF);
		hexStr[14-2*i]=int_to_hexString((rom[i]&0xF0)>>4);
	}
}

